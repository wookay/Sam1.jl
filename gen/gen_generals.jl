using ThreeKingdoms: 人名錄
function findfirstgeneral(name)
    g = findfirst(人名錄.登場人物) do 인물
        haskey(인물, :Name) && 인물.Name == name
    end
    if g === nothing
        nothing
    else
        人名錄.登場人物[g]
    end
end
